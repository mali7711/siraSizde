package tr.com.mali.sirasizde;

public class Arkadaslar {
    int okulNo;
    String Adi;
    String Soyad;
    String Sinif;
    int siraNo;
    int foto;

    public Arkadaslar(int okulNo, String adi, String soyad, String sinif, int siraNo, int foto) {
        this.okulNo = okulNo;
        Adi = adi;
        Soyad = soyad;
        Sinif = sinif;
        this.siraNo = siraNo;
        this.foto = foto;
    }
}
